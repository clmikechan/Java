package Ch02;

public class Ch02_04 {

	public static void main(String[] args) {
		int a='0';  //讀出'0'對應的整數值
		int b='A';  //讀出'A'對應的整數值
		int c='a';  //讀出'a'對應的整數值
		int d='張';  //讀出'張'對應的整數值
		int e='君';  //讀出'君'對應的整數值
		int f='雅';  //讀出'雅'對應的整數值
		System.out.println("0="+a);  //列印出'0'對應的整數值
		System.out.println("A="+b);  //列印出'A'對應的整數值
		System.out.println("a="+c);  //列印出'a'對應的整數值
		System.out.println("張="+d);  //列印出'張'對應的整數值
		System.out.println("君="+e);  //列印出'君'對應的整數值
		System.out.println("雅="+f);  //列印出'雅'對應的整數值
	}

}
